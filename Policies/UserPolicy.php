    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function viewAny(User $user) {
        $userRolls = UserRole::where('user_id', $user->id)->pluck('role_id')->toArray();
        return in_array(Role::IS_ADMIN, $userRolls)
            ? Response::allow()
            : Response::deny('You have no permission.', 403);
    }

